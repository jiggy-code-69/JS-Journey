// Using let
let myVariable = 'Hello World';
myVariable = 'Hi there'; // Valid - can reassign a new value to the variable
console.log(myVariable); // Output: 'Hi there'

// Using const
const myConstant = 'Hello World';
myConstant = 'Hi there'; // Invalid - cannot reassign a new value to a constant
console.log(myConstant); // Output: 'Hello World'

// Using var
var myVar = 'Hello World';
myVar = 'Hi there'; // Valid - can reassign a new value to the variable
console.log(myVar); // Output: 'Hi there'
